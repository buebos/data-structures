#include "operations.h"

float trex_add(float a, float b) {
    return a + b;
}
float trex_substract(float a, float b) {
    return a - b;
}
float trex_multiply(float a, float b) {
    return a * b;
}
float trex_divide(float a, float b) {
    return a / b;
}
float trex_pow(float a, float b) {
    return (float)pow(a, b);
}
